# –ø–æ–¥–∫–ª—é—á–∞–µ–º –≥—Ä–∞—Ñ–∏—á–µ—Å–∫—É—é –±–∏–±–ª–∏–æ—Ç–µ–∫—É
from tkinter import *
# –ø–æ–¥–∫–ª—é—á–∞–µ–º –º–æ–¥—É–ª–∏, –∫–æ—Ç–æ—Ä—ã–µ –æ—Ç–≤–µ—á–∞—é—Ç –∑–∞ –≤—Ä–µ–º—è –∏ —Å–ª—É—á–∞–π–Ω—ã–µ —á–∏—Å–ª–∞
import time
# —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π –æ–±—ä–µ–∫—Ç ‚Äî –æ–∫–Ω–æ —Å –∏–≥—Ä–æ–≤—ã–º –ø–æ–ª–µ–º. –í –Ω–∞—à–µ–º —Å–ª—É—á–∞–µ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –æ–∫–Ω–∞ –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è tk, –∏ –º—ã –µ–≥–æ —Å–¥–µ–ª–∞–ª–∏ –∏–∑ –∫–ª–∞—Å—Å–∞ Tk() ‚Äî –æ–Ω –µ—Å—Ç—å –≤ –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–µ 
tk = Tk()
# –¥–µ–ª–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ–∫–Ω–∞ ‚Äî Games —Å –ø–æ–º–æ—â—å—é —Å–≤–æ–π—Å—Ç–≤–∞ –æ–±—ä–µ–∫—Ç–∞ title
tk.title('–†–∞–∑–±–∏—Ä–∞–µ–º –û–û–ü')
# –∑–∞–ø—Ä–µ—â–∞–µ–º –º–µ–Ω—è—Ç—å —Ä–∞–∑–º–µ—Ä—ã –æ–∫–Ω–∞, –¥–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å–≤–æ–π—Å—Ç–≤–æ resizable 
tk.resizable(0, 0)
# –ø–æ–º–µ—â–∞–µ–º –Ω–∞—à–µ –∏–≥—Ä–æ–≤–æ–µ –æ–∫–Ω–æ –≤—ã—à–µ –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –æ–∫–æ–Ω –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–µ, —á—Ç–æ–±—ã –¥—Ä—É–≥–∏–µ –æ–∫–Ω–∞ –Ω–µ –º–æ–≥–ª–∏ –µ–≥–æ –∑–∞—Å–ª–æ–Ω–∏—Ç—å. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ üôÇ
tk.wm_attributes('-topmost', 1)
# —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Ö–æ–ª—Å—Ç ‚Äî 400 –Ω–∞ 500 –ø–∏–∫—Å–µ–ª–µ–π, –≥–¥–µ –∏ –±—É–¥–µ–º —Ä–∏—Å–æ–≤–∞—Ç—å –∏–≥—Ä—É
canvas = Canvas(tk, width=500, height=400, highlightthickness=0)
# –≥–æ–≤–æ—Ä–∏–º —Ö–æ–ª—Å—Ç—É, —á—Ç–æ —É –∫–∞–∂–¥–æ–≥–æ –≤–∏–¥–∏–º–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –±—É–¥—É—Ç —Å–≤–æ–∏ –æ—Ç–¥–µ–ª—å–Ω—ã–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã 
canvas.pack()
# –æ–±–Ω–æ–≤–ª—è–µ–º –æ–∫–Ω–æ —Å —Ö–æ–ª—Å—Ç–æ–º
tk.update()
    
class Ball:
    # –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä ‚Äî –æ–Ω –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –≤ –º–æ–º–µ–Ω—Ç —Å–æ–∑–¥–∞–Ω–∏—è –Ω–æ–≤–æ–≥–æ –æ–±—ä–µ–∫—Ç–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ —ç—Ç–æ–≥–æ –∫–ª–∞—Å—Å–∞
    def __init__(self, canvas, color, x, y, up, down, left, right):
        # –∑–∞–¥–∞—ë–º –∞—Ç—Ä–∏–±—É—Ç—ã (–Ω-—Ä: self.canvas, –≥–¥–µ .canvas - —ç—Ç–æ —Å–≤–æ–π—Å—Ç–≤–æ –∫–ª–∞—Å—Å–∞) –Ω–æ–≤–æ–≥–æ –æ–±—ä–µ–∫—Ç–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏
        # –∏–≥—Ä–æ–≤–æ–µ –ø–æ–ª–µ
        self.canvas = canvas
        # –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã
        self.x = 0
        self.y = 0
        # —Ü–≤–µ—Ç –Ω—É–∂–µ–Ω –±—ã–ª –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –º—ã –∏–º –∑–∞–∫—Ä–∞—Å–∏–ª–∏ –≤–µ—Å—å —à–∞—Ä–∏–∫
        # –∑–¥–µ—Å—å –ø–æ—è–≤–ª—è–µ—Ç—Å—è –Ω–æ–≤–æ–µ —Å–≤–æ–π—Å—Ç–≤–æ id, –≤ –∫–æ—Ç–æ—Ä–æ–º —Ö—Ä–∞–Ω–∏—Ç—Å—è –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ —à–∞—Ä–∏–∫–∞
        # –∞ –µ—â—ë –∫–æ–º–∞–Ω–¥–æ–π create_oval –º—ã —Å–æ–∑–¥–∞—ë–º –∫—Ä—É–≥ —Ä–∞–¥–∏—É—Å–æ–º 15 –ø–∏–∫—Å–µ–ª–µ–π –∏ –∑–∞–∫—Ä–∞—à–∏–≤–∞–µ–º –Ω—É–∂–Ω—ã–º —Ü–≤–µ—Ç–æ–º
        self.id = canvas.create_oval(10,10, 25, 25, fill=color)
        # –ø–æ–º–µ—â–∞–µ–º —à–∞—Ä–∏–∫ –≤ —Ç–æ—á–∫—É —Å –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–º–∏ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞–º–∏
        self.canvas.move(self.id, x, y)
        # –µ—Å–ª–∏ –Ω–∞–∂–∞—Ç–∞ —Å—Ç—Ä–µ–ª–∫–∞ –≤–ø—Ä–∞–≤–æ ‚Äî –¥–≤–∏–≥–∞–µ–º—Å—è –≤–ø—Ä–∞–≤–æ
        self.canvas.bind_all(right, self.turn_right)
        # –≤–ª–µ–≤–æ
        self.canvas.bind_all(left, self.turn_left)
        # –Ω–∞–≤–µ—Ä—Ö
        self.canvas.bind_all(up, self.turn_up)
        # –≤–Ω–∏–∑
        self.canvas.bind_all(down, self.turn_down)
        # —à–∞—Ä–∏–∫ –∑–∞–ø–æ–º–∏–Ω–∞–µ—Ç —Å–≤–æ—é –≤—ã—Å–æ—Ç—É –∏ —à–∏—Ä–∏–Ω—É
        self.canvas_height = self.canvas.winfo_height()
        self.canvas_width = self.canvas.winfo_width()

    # –¥–≤–∏–∂–µ–º—Å—è –≤–ø—Ä–∞–≤–æ
    # —Å–º–µ—â–∞–µ–º—Å—è –Ω–∞ 2 –ø–∏–∫—Å–µ–ª—è –≤ —É–∫–∞–∑–∞–Ω–Ω—É—é —Å—Ç–æ—Ä–æ–Ω—É
    def turn_right(self, event):
        # –ø–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —à–∞—Ä–∏–∫–∞
        pos = self.canvas.coords(self.id)
        # –µ—Å–ª–∏ –Ω–µ –≤—ã—à–ª–∏ –∑–∞ –≥—Ä–∞–Ω–∏—Ü—ã —Ö–æ–ª—Å—Ç–∞
        if not pos[2] >= self.canvas_width:
            # –±—É–¥–µ–º —Å–º–µ—â–∞—Ç—å—Å—è –ø—Ä–∞–≤–µ–µ –Ω–∞ 2 –ø–∏–∫—Å–µ–ª—è –ø–æ –æ—Å–∏ —Ö
            self.x = 2
            self.y = 0

    # –≤–ª–µ–≤–æ
    def turn_left(self, event):
        pos = self.canvas.coords(self.id)
        if not pos[0] <= 0:
            self.x = -2
            self.y = 0
    # –≤–≤–µ—Ä—Ö
    def turn_up(self, event):
        pos = self.canvas.coords(self.id)
        if not pos[1] <= 0:
            self.x = 0
            self.y = -2

    # –≤–Ω–∏–∑
    def turn_down(self, event):
        pos = self.canvas.coords(self.id)
        if not pos[3] >= self.canvas_height:
            self.x = 0
            self.y = 2
                
    # –º–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–≤–µ—á–∞–µ—Ç –∑–∞ –æ—Ç—Ä–∏—Å–æ–≤–∫—É —à–∞—Ä–∏–∫–∞ –Ω–∞ –Ω–æ–≤–æ–º –º–µ—Å—Ç–µ
    def draw(self):
        # –ø–µ—Ä–µ–¥–≤–∏–≥–∞–µ–º —à–∞—Ä–∏–∫ –Ω–∞ –∑–∞–¥–∞–Ω–Ω—ã–π –≤–µ–∫—Ç–æ—Ä x –∏ y
        self.canvas.move(self.id, self.x, self.y)
        # –∑–∞–ø–æ–º–∏–Ω–∞–µ–º –Ω–æ–≤—ã–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —à–∞—Ä–∏–∫–∞
        pos = self.canvas.coords(self.id)
        
        # –µ—Å–ª–∏ –∫–æ—Å–Ω—É–ª–∏—Å—å –ª–µ–≤–æ–π —Å—Ç–µ–Ω–∫–∏
        if pos[0] <= 0:
            # –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º—Å—è
            self.x = 0
        # –≤–µ—Ä—Ö–Ω–µ–π
        if pos[1] <= 0:
            self.y = 0
        # –ø—Ä–∞–≤–æ–π
        if pos[2] >= self.canvas_width:
            self.x = 0
        # –Ω–∏–∂–Ω–µ–π
        if pos[3] >= self.canvas_height:
            self.y = 0
        
# —Å–æ–∑–¥–∞—ë–º —à–∞—Ä–∏–∫ ‚Äî –æ–±—ä–µ–∫—Ç –Ω–∞ –æ—Å–Ω–æ–≤–µ –∫–ª–∞—Å—Å–∞
ball_one = Ball(canvas,'red', 150, 150,  '<KeyPress-Up>', '<KeyPress-Down>', '<KeyPress-Left>', '<KeyPress-Right>')
# —Å–æ–∑–¥–∞—ë–º –≤—Ç–æ—Ä–æ–π —à–∞—Ä–∏–∫ ‚Äî –¥—Ä—É–≥–æ–π –æ–±—ä–µ–∫—Ç –Ω–∞ –æ—Å–Ω–æ–≤–µ —ç—Ç–æ–≥–æ –∂–µ –∫–ª–∞—Å—Å–∞, –Ω–æ —Å –¥—Ä—É–≥–∏–º–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
ball_two = Ball(canvas,'blue', 100, 100,  '<w>', '<s>', '<a>', '<d>')


# –∑–∞–ø—É—Å–∫–∞–µ–º –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª
while not False:

    # –æ–±–Ω–æ–≤–ª—è–µ–º –Ω–∞—à–µ –∏–≥—Ä–æ–≤–æ–µ –ø–æ–ª–µ, —á—Ç–æ–±—ã –≤—Å—ë, —á—Ç–æ –Ω—É–∂–Ω–æ, –∑–∞–∫–æ–Ω—á–∏–ª–æ —Ä–∏—Å–æ–≤–∞—Ç—å—Å—è
    tk.update_idletasks()
    # –æ–±–Ω–æ–≤–ª—è–µ–º –∏–≥—Ä–æ–≤–æ–µ –ø–æ–ª–µ –∏ —Å–º–æ—Ç—Ä–∏–º –∑–∞ —Ç–µ–º, —á—Ç–æ–±—ã –≤—Å—ë, —á—Ç–æ –¥–æ–ª–∂–Ω–æ –±—ã–ª–æ –±—ã—Ç—å —Å–¥–µ–ª–∞–Ω–æ ‚Äî –±—ã–ª–æ —Å–¥–µ–ª–∞–Ω–æ
    tk.update()
    # –∑–∞–º–∏—Ä–∞–µ–º –Ω–∞ –æ–¥–Ω—É —Å–æ—Ç—É—é —Å–µ–∫—É–Ω–¥—ã, —á—Ç–æ–±—ã –¥–≤–∏–∂–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤—ã–≥–ª—è–¥–µ–ª–æ –ø–ª–∞–≤–Ω–æ
    time.sleep(0.01)
    # —Ä–∏—Å—É–µ–º —à–∞—Ä–∏–∫ 1
    ball_one.draw()
    # —Ä–∏—Å—É–µ–º —à–∞—Ä–∏–∫ 2
    ball_two.draw()
    
